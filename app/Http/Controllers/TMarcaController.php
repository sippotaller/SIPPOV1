    {
        $v=Validator::make($request->all(),[
            'desc'=>'required'
            ]);
        if(!$v->fails()){
            $id=TMarca::create($request->all());
            return array("status"=>"Exito","object"=>$id);
        }else{
            return array("status"=>"Error","messages"=>$v->errors());
        }
        
    }

